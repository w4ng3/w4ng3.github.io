<p>他如此温柔地对待那可怕的，同时又极易损伤的脆弱的怪物。我觉得他的这个举动再好不过地体现了他所代表的那些轻微的物质。最令人意想不到的是，这时出现了奇迹：海藻接触到美杜莎的血液后变成了珊瑚；仙女们赶来观赏珊瑚并把各种海藻抛向那可怕的头颅。</p>
