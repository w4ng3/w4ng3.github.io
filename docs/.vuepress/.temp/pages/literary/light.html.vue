<h2 id="柏尔修斯的力量" tabindex="-1"><a class="header-anchor" href="#柏尔修斯的力量" aria-hidden="true">#</a> 柏尔修斯的力量</h2>
<p><strong>柏尔修斯为了割下美杜莎的头颅，避免自己变成石头，他依靠的是世界上最轻的物质——风和云，并把自己的目光投向间接的形象——镜面反射的映象</strong>。</p>
<p>柏尔修斯与这个戈耳工女妖之间的关系很复杂。他们的关系并未因割下女妖的头而结束：从美杜莎流出的血液中又跃出了一匹飞马，叫珀伽索斯；石头的沉重也可以变成它的对立面；珀伽索斯用蹄子在赫利孔山上一踢，踢出了供缪斯女神饮水的山泉。这个神话有许多版本，有些版本说，这匹从美杜莎血泊中跳出来的与缪斯女神关系密切的飞马，成了柏尔修斯的坐骑。（另外，飞行鞋也来自妖魔世界：柏尔修斯是从美杜莎的同类，独眼女妖格赖埃那里得到那双飞行鞋的。）还有美杜莎的头，柏尔修斯并未将它扔掉，而是把它装在皮囊里带在身边；如果在战争中遇到危险，他只要抓住那由毒蛇构成的头发把血淋淋的头颅掏出来，那颗头便成了他克敌制胜的武器。这件武器他仅在危急情况下使用，而且仅仅用来对付那些只配化为石头人的对手。这个细节肯定想告诉我们某种道理，而且把这个道理寓意于形象之中，避免用别的方法进行说明。柏尔修斯现在控制那可怕的面孔的方法是，把它藏在皮囊里，而原先战胜它的方法是，通过镜面的反射来看它。==柏尔修斯的力量在于，始终拒绝正面观察，而不是拒绝与妖魔相处==。他甚至把妖魔的头带在身边，并作为负荷背在肩上。</p>
